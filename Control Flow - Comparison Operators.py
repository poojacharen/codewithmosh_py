# Comparison Operators : It is used to compare values
# eg:
# 10 > 4  True
# 10 >= 4 True
# 10 < 40  True
# 10 <= 40  True
# 10 == 10  True
# 10 == "10" False , because this value has different types and it is stored diff in computer memory
# 10 != 10  True
# "bag" > "apple"  True
# "bag" == "BAG"  False

