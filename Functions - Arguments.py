# Arguments

# Parameters : They are the input that is defined for the function
# Argument : This is the actual value for a given parameter

def greet(first_name, last_name):      # greet(): inside the brackets if mentioned anything are called parameters
    print(f"Hi {first_name} {last_name} ")
    print("Welcome aboard")

greet("Poojitha", "Nagallapati")
greet("Charen", "Koneti")# here inside the brackets they are called as arguments
